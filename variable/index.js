// Création d'une variable non constante
let name = "Legolas";

// Affichage de la variable
console.log(name);

// Modification de la variable non constante
name = "Gimli";

// Affichage après modification
console.log(name);



// Création d'un très grand nombre entier
let bigNumber = 12345678901234567890;
console.log(bigNumber);

// Création d'un nombre avec beaucoup de décimales
let decimalNumber = 3.14159265358979323846;
console.log(decimalNumber);

// Création d'un nombre négatif
let negativeNumber = -100;
console.log(negativeNumber);

// Création de variables booléennes
